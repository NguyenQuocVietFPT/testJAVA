    private void timeChay() {
        Thread thread1 = new Thread() {
            
            
            @Override
            public void run() {
                
                while (true) {
                    Date now = new Date();
                    
                    SimpleDateFormat format = new SimpleDateFormat();
                    format.applyPattern("hh:mm:ss");

                    String time = format.format(now);

                    try {
                        Thread.sleep(1000);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(QuanLyKhachHang.class.getName()).log(Level.SEVERE, null, ex);
                    }

                    lbl_tme.setText(time);
                }
            }

        };
        thread1.start();
    }

    private void fillTotable() {
        tableModel.setRowCount(0);

        for (KhachHang khachHang : khachhangList) {
            tableModel.addRow(new Object[]{
                khachHang.getLoai(), khachHang.getMa(), khachHang.getTen(), khachHang.getEmail()
            });
        }
    }

    private void clear() {
        cbo_loai.setSelectedIndex(0);
        txt_ma.setText("");
        txt_ten.setText("");
        txt_email.setText("");
    }

    private void saveFile() {
        FileOutputStream fos = null;
        ObjectOutputStream oos = null;

        try {
            fos = new FileOutputStream(fileURL);
            oos = new ObjectOutputStream(fos);
            
            oos.writeObject(khachhangList);
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(QuanLyKhachHang.class.getName()).log(Level.SEVERE, null, ex);        
        } catch (IOException ex) {
            Logger.getLogger(QuanLyKhachHang.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            if(oos != null){
                try {
                    oos.close();
                } catch (IOException ex) {
                    Logger.getLogger(QuanLyKhachHang.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            
            if(fos != null){
                try {
                    fos.close();
                } catch (IOException ex) {
                    Logger.getLogger(QuanLyKhachHang.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }        
    }

    private ArrayList<KhachHang> openFile() {        
        ArrayList<KhachHang> file = new ArrayList<>();
        try {
            FileInputStream fis = new FileInputStream(fileURL);
            ObjectInputStream ois = new ObjectInputStream(fis);

            file = (ArrayList<KhachHang>) ois.readObject();
            
            ois.close();
            fis.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(QuanLyKhachHang.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(QuanLyKhachHang.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(QuanLyKhachHang.class.getName()).log(Level.SEVERE, null, ex);
        } 
        
        return file;
    }


    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:

        int index = tbl_khachhang.getSelectedRow();
        
        if (index >= 0) {
            KhachHang khachHang = khachhangList.get(index);
            int optin = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa khách hàng này không");
            if (optin == 0) {
                khachhangList.remove(khachHang);
                JOptionPane.showMessageDialog(this, "Xóa thành công");
                clear();
                fillTotable();
                saveFile();
            }
            else{
                fillTotable();
            }
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

    private void btn_openfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_openfileActionPerformed
        // TODO add your handling code here:
        khachhangList = openFile();

        if (khachhangList.isEmpty()) {
            JOptionPane.showMessageDialog(this, "File trống");
        } else {
            JOptionPane.showMessageDialog(this, "Mở file thành công");
            KhachHang kh = khachhangList.get(0);
            if(kh.getLoai().equals("VIP")){
                cbo_loai.setSelectedIndex(0);
            }
            else{
                cbo_loai.setSelectedIndex(1);
            }
            
            txt_ma.setText(kh.getMa());
            txt_ten.setText(kh.getTen());
            txt_email.setText(kh.getEmail());
            
            tableModel.setRowCount(0);

            for (KhachHang khachHang : khachhangList) {
                tableModel.addRow(new Object[]{
                    khachHang.getLoai(), khachHang.getMa(), khachHang.getTen(), khachHang.getEmail()
                });
            }
        }
    }//GEN-LAST:event_btn_openfileActionPerformed


    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:
        String loai = cbo_loai.getSelectedItem().toString();

        String name = txt_ten.getText();
        int checkTen = 0;

        if (name.isEmpty()) {
            checkTen = 1;
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập tên");
        }

        String ma = txt_ma.getText();
        int checkma = 0;
        if (ma.isEmpty()) {
            checkma = 1;
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập mã");
        }

        String email = txt_email.getText();
        int checkemail = 0;

        if (email.isEmpty()) {
            checkemail = 1;
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập email");
        } else if (!email.contains("@")) {
            checkemail = 1;
            JOptionPane.showMessageDialog(this, "Email phải có ký tự @");
        }

        if (checkTen == 0 && checkma == 0 && checkemail == 0) {
            KhachHang khachHang = new KhachHang(loai, ma, name, email);
            khachhangList.add(khachHang);
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            fillTotable();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm không thành công");
        }
    }//GEN-LAST:event_btn_addActionPerformed

